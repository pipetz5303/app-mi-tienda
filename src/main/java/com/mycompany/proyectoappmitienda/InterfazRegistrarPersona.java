    private void jBtnRegPersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnRegPersonaActionPerformed
        //try para validar info
        try {
            String nombre = jTxtFNombre.getText();
            String apellido = jTxtFApellido.getText();
            String tipoDNI = jCBTipoDNI.getSelectedItem().toString();
            String numero = jTxtFNumeroDNI.getText().trim();

            int numeroMesa = Integer.parseInt(jCBMesa.getSelectedItem().toString());

            if ((!nombre.matches("") && !nombre.matches("nombre")) && (!nombre.matches("") && !nombre.matches("nombre"))) {
                //almacernar variables
                Persona p = new Persona();
                p.setIdDNIPersona(numero);
                p.setNombre(nombre);
                p.setApeliido(apellido);
                p.setTipoDNI(tipoDNI);
                p.setNumeroMesa(numeroMesa);
               
                
                crud.registrarPersona(condb, p, idGrupoI);

                limpiarCasillas();
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Revise elementos e intente de nuevo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            System.out.println("Error: " + ex);
        }


    }//GEN-LAST:event_jBtnRegPersonaActionPerformed

