    public void cargarTiposDNI() {
        listaDocumentos = crud.obtenerTiposDNI(condb);
        jCBTipoDNI.removeAllItems();
        for (TipoDNI tipo : listaDocumentos) {
            jCBTipoDNI.addItem(String.valueOf(tipo.getTipoDNI()));
        }
    }

    public void revisarMesas(int mesaSeleccionada) {
        listaMesas = crud.obtenerMesas(condb);
        for (Mesa m : listaMesas) {
            jCBMesa.addItem(String.valueOf(m.getNumeroMesa()));
        }

        jCBMesa.setSelectedIndex(mesaSeleccionada);

    }

