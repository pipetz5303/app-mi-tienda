    //buscar el valor
    private void jBtnBuscarCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBuscarCatActionPerformed
        //recibe el valor. Compara que no sea vacio
        String categoria = jTxtFNombre.getText().trim().toLowerCase();
        //si no es vacio, y no existe. Lo busca. Si lo es muestra mensaje
        con = new Conexion();
        condb = con.ObtenerConexion();
        crud = new CRUDSqlServer();
                
        if(!categoria.matches("") && !categoria.matches("nombre")){
            Categoria c = new Categoria();
            c.setNombre(categoria);
            c = crud.buscarCategoria(condb, c);
            jTxtFCategoria.setText(c.getNombre());
            jTxtFIdCategoria.setText(String.valueOf(c.getId()));
        }else{
            JOptionPane.showMessageDialog(rootPane, "Nombre ingresado no es v√°lido. Intente de nuevo", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBtnBuscarCatActionPerformed

    private void jBtnActualizarCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnActualizarCatActionPerformed
        con = new Conexion();
        condb = con.ObtenerConexion();
        crud = new CRUDSqlServer();
             
        Categoria c = new Categoria();
        c.setId(Integer.parseInt(jTxtFIdCategoria.getText()));
        c.setNombre(jTxtFCategoria.getText().trim().toLowerCase());

        crud.actualizarCategoria(condb, c);
     
    }//GEN-LAST:event_jBtnActualizarCatActionPerformed

