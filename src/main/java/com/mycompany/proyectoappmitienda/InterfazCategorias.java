    public void imprimirCategorias(){
        //defino el modelo de la tabla y le indico como se llama
        DefaultTableModel modelo;
        modelo = (DefaultTableModel) jTCategorias.getModel();
        
        //Limpiar la tabla
        int i = 0;
        if(modelo.getRowCount()!=0){
            while(i!=-1){
                try{
                    modelo.removeRow(modelo.getRowCount()-1);
                }catch(Exception e){
                    i=-1;
                }
            }
        }
        try {
            
            List<Categoria> ListaC = ListaCategorias;
            Iterator<Categoria> it = ListaC.iterator();
            //como uso dos columnas el arreglo es de 2
            String [] obj = new String[2];
            while(it.hasNext()){
                Categoria c = it.next();
                obj[0] = String.valueOf(c.getId());
                obj[1] = c.getNombre();
                modelo.addRow(obj);
            }            
        }catch(Exception e){
            System.out.println("Error: "+e);
        }
    }
    
    
