    private void jBtnRegCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnRegCategoriaActionPerformed
        //recibe el valor. Compara que no sea vacio
        String categoria = jTxtFNombre.getText().trim().toLowerCase();
        //si no es vacio, y no existe. Lo registra. Si lo es muestra mensaje
        
        if(!categoria.matches("") && !categoria.matches("nombre")){
            Categoria c = new Categoria();
            c.setNombre(categoria);
            crud.insertarCategoria(condb, c);
            //limpia el campo
           jTxtFNombre.setText("Nombre");
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Nombre ingresado no es v√°lido. Intente de nuevo", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jBtnRegCategoriaActionPerformed

